// Output: false
// Explanation: Same as Example 1, except with the 5 in the top left corner being modified to 8. Since there are two 8's in the top left 3x3 sub-box, it is invalid.

function isValidSudoku(board: string[][]): boolean {
  const rows = Array.from({ length: 9 }, () => new Set<string>());
  const columns = Array.from({ length: 9 }, () => new Set<string>());
  const squares = Array.from({ length: 9 }, () => new Set<string>());

  for (let row = 0; row < board.length; row++) {
    for (let col = 0; col < board[row]!.length; col++) {
      const curr = board[row]![col]!;

      if (curr === ".") continue;

      const square = Math.floor(row / 3) * 3 + Math.floor(col / 3);

      if (
        rows[row]!.has(curr) ||
        columns[col]!.has(curr) ||
        squares[square]!.has(curr)
      ) {
        return false;
      }

      rows[row]!.add(curr);
      columns[col]!.add(curr);
      squares[square]!.add(curr);
    }
  }

  return true;
}

